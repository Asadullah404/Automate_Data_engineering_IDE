def prompt_file(title="Select File"):
    """
    A special function injected into the Python sandbox. 
    It pauses execution and prompts the user to select a file using a native OS window.
    """
    root = tk.Tk()
    root.withdraw() # Hide the main tk window
    root.attributes('-topmost', True) # Force to front
    file_path = filedialog.askopenfilename(title=title, filetypes=[("Excel/CSV files", "*.xlsx *.csv"), ("All files", "*.*")])
    root.destroy()
    return file_path

def confirm_choice(title, message):
    """Prompts the user with a Yes/No dialog."""
    root = tk.Tk()
    root.withdraw()
    root.attributes('-topmost', True)
    result = messagebox.askyesno(title, message)
    root.destroy()
    return result

class OmniEvaluator:
    """Handles parsing and executing raw Python in a multi-dataframe environment."""
    
    @staticmethod
    def execute_python_logic(dfs_dict, vars_dict, code_block, script_path=None):
        # Load all dataframes, variables, and our special prompt function into the environment
        local_env = {**dfs_dict, **vars_dict, 'np': np, 'pd': pd, 'os': os, 'sys': sys, 'prompt_file': prompt_file}
        
        # FIX: Inject standard dunder variables so external Tkinter scripts can initialize properly
        local_env['__name__'] = '__main__'
        if script_path:
            local_env['__file__'] = script_path
        else:
            local_env['__file__'] = '<string>'
            
        try:
            # Pass local_env as BOTH globals and locals so functions can see imported modules like 'tk'
            exec(code_block, local_env, local_env)
            
            # Extract out the DataFrames (including those generated by external tkinter scripts)
            updated_dfs = {k: v for k, v in local_env.items() if isinstance(v, pd.DataFrame)}
            
            # Extract out the variables for debugging
            updated_vars = {}
            for k, v in local_env.items():
                if not isinstance(v, pd.DataFrame) and not callable(v) and not k.startswith('_') and str(type(v).__module__) == 'builtins':
                    updated_vars[k] = v
                        
            return updated_dfs, updated_vars
        except Exception as e:
            raise RuntimeError(f"Python Logic Error in block:\n{code_block}\nError: {str(e)}")

